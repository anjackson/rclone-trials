#!/bin/sh

mkdir testdata
dd if=/dev/zero of=testdata/1m.bin bs=1M count=1

echo "------------------------ LSR ---------------------------"
rclone lsl h3:/

echo "--------------------------------------------------------"
echo "--------------------------------------------------------"
echo "------------------------ COPY --------------------------"
echo "--------------------------------------------------------"
echo "--------------------------------------------------------"
rclone copy -vv testdata h3:/user/root/testdata
echo "------------------------ LIST --------------------------"
rclone lsl --max-depth 1 h3:/user/root/testdata
echo "------------------------ DUMP ---------------------------"
rclone backend fulldump h3-hasher:


echo "--------------------------------------------------------"
echo "--------------------------------------------------------"
echo "------------------------ COPY2 -------------------------"
echo "--------------------------------------------------------"
echo "--------------------------------------------------------"
rclone copy -vv --checksum testdata h3:/user/root/testdata

echo "--------------------------------------------------------"
echo "--------------------------------------------------------"
echo "------------------------ CHECK --------------------------"
echo "--------------------------------------------------------"
echo "--------------------------------------------------------"
rclone check -vv --download testdata h3-hasher:/user/root/testdata
echo "------------------------ DUMP ---------------------------"
rclone backend fulldump h3-hasher:

echo "--------------------------------------------------------"
echo "--------------------------------------------------------"
echo "------------------------ MOVE --------------------------"
echo "--------------------------------------------------------"
echo "--------------------------------------------------------"
rclone move -vv --delete-empty-src-dirs --checksum testdata h3-hasher:/user/root/testdata
echo "------------------------ DUMP ---------------------------"
rclone backend fulldump h3-hasher:

echo "--------------------------------------------------------"
echo "--------------------------------------------------------"
echo "------------------------ GONE --------------------------"
echo "--------------------------------------------------------"
echo "--------------------------------------------------------"
ls -la testdata

